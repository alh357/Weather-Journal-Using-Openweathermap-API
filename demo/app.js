form.addEventListener("submit", addCard);

async function addCard(e) {
  e.preventDefault();
  id += 1;
  const task = await sendZip("http://localhost:8000/zip", id);
  if (task) {
    if (task[id].status === 200) {
      a = [];
      let entry = document.createElement("div");
      entry.classList.add("entry");
      let cite = document.createElement("div");
      cite.textContent = `${task[id].name}, ${countries[task[id].country]}`;
      cite.classList.add("loc");
      a.push(cite);
      const day = document.createElement("div");
      day.classList.add("day");
      day.textContent = days[date.getDay()];
      a.push(day);
      let dat = document.createElement("div");
      dat.textContent = `${date.getDate()}/${date.getMonth()}/${date.getUTCFullYear()}`;
      dat.classList.add("date");
      a.push(dat);
      let img = document.createElement("img");
      img.setAttribute(
        "src",
        `https://openweathermap.org/img/wn/${task[id].weath.icon}@2x.png`
      );
      img.classList.add("img");
      a.push(img);
      let temp = document.createElement("div");
      temp.textContent = `${(+task[id].main.temp - 273.15).toFixed(1)}℃`;
      temp.classList.add("temp");
      a.push(temp);
      const cond = document.createElement("div");
      cond.classList.add("weath");
      cond.textContent = task[id].weath.description;
      a.push(cond);
      let feel = document.createElement("div");
      feel.textContent = note.value;
      feel.classList.add("note");
      a.push(feel);
      let x = document.createElement("div");
      x.textContent = "✖";
      x.classList.add("x");
      entry.appendChild(x);
      exes.push(x);
      for (i of a) {
        i.classList.add("ent");
        entry.appendChild(i);
      }
      entries.appendChild(entry);
    } else {
      p = document.createElement("p");
      p.textContent = task[id].status;
      p.style.textAlign = "center";
      p.style.fontSize = "2em";
      cont.appendChild(p);
      setTimeout(() => {
        p.remove();
      }, 3000);
    }
    exes.forEach((i) => {
      i.addEventListener("click", () => {
        console.log("ala");
        i.parentNode.remove();
      });
    });
  }
  console.log(task);
}

const sendZip = async (url = "", data) => {
  try {
    const response = await fetch(url, {
      method: "POST",
      credentials: "same-origin",
      headers: {
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        zip: city.value,
        feeli: note.value,
        id: data,
      }),
    });

    if (!response.ok) {
      throw new Error(`HTTP error! status: ${response.status}`);
    }

    const newData = await response.json();
    return newData;
  } catch (error) {
    // p = document.createElement("p");
    // p.textContent = "Network Error";
    // //p.style.color = "red";
    // p.style.fontSize = "2em";
    // p.style.textAlign = "center";
    // form.appendChild(p);
    // setTimeout(() => {
    //   p.remove();
    // }, 3000);
    console.log("error");
    return null;
  }
};
